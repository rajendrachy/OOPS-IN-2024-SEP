// __________________________Static Class members________________________________________________-
// Static Class members (Static data members & Static member functions)

// The Syntax is

// static <variable definition>
// static <function definition>


// Examples are

// static int c;
// static void display ( )
// {
// 	…………….
// }


// A static member is referenced via scope resolution operator in the form of
// ClassName :: VariableName or ClassName :: FunctionName( ).

// It can be used to implement "global" class variables and functions that can 
// be used without creating instances of a class.

